  function _patchPaymentConfirmedSlotUpdate() {
    window.addEventListener('bmg:paymentConfirmed', async (e) => {
      const { paymentType, result } = e.detail || {};
      if (paymentType !== 'booking') return;
      if (!result) return;

      const db = _db();
      if (!db) return;

      try {
        const groundId  = result.groundId  || '';
        const date      = result.date      || '';
        const slotTime  = result.slotTime  || '';
        const bookingId = result.bookingId || e.detail.orderId || '';

        if (!groundId || !date || !slotTime) return;

        const startTime = slotTime.split('-')[0].trim();

        // Find the slot and update it to booked
        const snap = await db.collection('slots')
          .where('groundId',  '==', groundId)
          .where('date',      '==', date)
          .where('startTime', '==', startTime)
          .limit(1).get();

        if (!snap.empty) {
          await snap.docs[0].ref.update({
            status        : 'booked',
            bookingId     : bookingId,
            lockOrderId   : null,
            lockBookingId : null,
            lockExpiresAt : null,
            lockExpiresAtMs: null,
            updatedAt     : firebase.firestore.FieldValue.serverTimestamp(),
          });
          console.log('[bmg-fix] ✅ Slot marked booked on frontend:', startTime, date);
        }
      } catch (err) {
        console.warn('[bmg-fix] Slot update on payment confirmed failed (non-fatal):', err.message);
      }
    });
  }


